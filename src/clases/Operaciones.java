public class Operaciones {
    
    public int suma(int numeroUno, int numeroDos){
        return numeroUno + numeroDos;
    }
    
    public float suma(float numeroUno, float numeroDos){
        return numeroUno + numeroDos;
    }
    
    public int resta(int numeroUno, int numeroDos){
        return numeroUno - numeroDos;
    }
    
    public float resta(float numeroUno, float numeroDos){
        return numeroUno - numeroDos;
    }
    
    public int multiplicacion(int numeroUno, int numeroDos){
        return numeroUno * numeroDos;
    }
    
    public float multiplicacion(float numeroUno, float numeroDos){
        return numeroUno * numeroDos;
    }
    
    public float division(int numeroUno, int numeroDos){
        return Float.valueOf(numeroUno) / Float.valueOf(numeroDos);
    }
    
    public float division(float numeroUno, float numeroDos){
        return numeroUno/numeroDos;
    }
}
