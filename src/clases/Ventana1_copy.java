    private void btnSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaActionPerformed
        String numeroUno,numeroDos;
        numeroUno = this.txtNumeroUno.getText();
        numeroDos = this.txtNumeroDos.getText();
        float numeroUnoFloat, numeroDosFloat;
        
        if(this.validacion.validarNumeros(numeroUno).equals("decimal")){
            //JOptionPane.showMessageDialog(null,"el numero es un entero");
            //Preguntas de decimal - entero y decimal - decimal Asi como entero - decimal y entero - entero
            if(this.validacion.validarNumeros(numeroDos).equals("decimal")){
                numeroUnoFloat = Float.parseFloat(numeroUno);
                numeroDosFloat = Float.parseFloat(numeroDos);    
                this.lblResultado.setText(Float.toString(this.operacion.suma(numeroUnoFloat, numeroDosFloat)));
            }else if(this.validacion.validarNumeros(numeroDos).equals("entero")){
                numeroUnoFloat = Float.parseFloat(numeroUno);
                numeroDosFloat = Float.parseFloat(numeroDos);
                this.lblResultado.setText(Float.toString(this.operacion.suma(numeroUnoFloat, numeroDosFloat)));
            }
            
        }else if(this.validacion.validarNumeros(numeroUno).equals("entero")){
            
            if(this.validacion.validarNumeros(numeroDos).equals("entero")){
                int numeroUnoInt = Integer.parseInt(numeroUno);
                int numeroDosInt = Integer.parseInt(numeroDos);
                this.lblResultado.setText(Integer.toString(this.operacion.suma(numeroUnoInt, numeroDosInt)));
                
            }else if(this.validacion.validarNumeros(numeroDos).equals("decimal")){
               numeroUnoFloat = Float.parseFloat(numeroUno);
               numeroDosFloat = Float.parseFloat(numeroDos);
               this.lblResultado.setText(Float.toString(this.operacion.suma(numeroUnoFloat, numeroDosFloat)));
            }
            
        }
    }//GEN-LAST:event_btnSumaActionPerformed

