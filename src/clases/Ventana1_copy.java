    private void txtNumeroUnoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumeroUnoKeyTyped
         //casting implicito de char a ascii, gracias a que un caracter cabe en los 8bits de un entero
        int key = evt.getKeyChar();
        if(key == 8 && contador > 0){
           contador--;
        }
        if((key >= 48 && key <= 57) || key == 46){
            contador++;
        }else{
            evt.consume();
        }
        if(contador > 0 && contador2 > 0){
            this.btnSuma.setEnabled(true);
            this.btnResta.setEnabled(true);
            this.btnMultiplicacion.setEnabled(true);
            this.btnDivision.setEnabled(true);
            this.btnCalcular.setEnabled(true);
            //.....
        }else{
            /*Event.consume() eso quiere decir que el evento se consumirá
            e impide en este caso, el ingreso de mas caracteres.*/
            //evt.consume();
            this.btnCalcular.setEnabled(false);
            this.btnDivision.setEnabled(false);
            this.btnMultiplicacion.setEnabled(false);
            this.btnResta.setEnabled(false);
            this.btnSuma.setEnabled(false);
        }
    }//GEN-LAST:event_txtNumeroUnoKeyTyped

